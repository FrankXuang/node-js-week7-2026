-- 工單 6：改寫這條查詢，讓它用到「已經存在」的索引
--
-- 原本的查詢寫法，因為函式包住欄位（`DATE(created_at)`），導致索引無法被用到：
--   SELECT count(*) AS total FROM course_bookings
--   WHERE DATE(created_at) = DATE '2026-06-24';
--
-- 你的任務：寫一個結果跟原本的查詢一樣、但不把欄位包進函式的查詢
-- （要查的是 2026-06-24 台北時間一整天的報名數，結果必須和原本的查詢完全一致）
-- ⚠️ 這題不能建任何新索引（營運反映寫入已經夠慢了），只能從改寫查詢本身來處理
-- ============================================================

-- 把你的改寫版本撰寫於下方：
SELECT count(*) AS total 
FROM course_bookings 
WHERE created_at >= TIMESTAMPTZ '2026-06-24 00:00:00+08'
  AND created_at <  TIMESTAMPTZ '2026-06-25 00:00:00+08';
